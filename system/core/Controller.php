/**
 * Application Controller Class
 *
 * This class object is the super class that every library in
 * CodeIgniter will be assigned to.
 *
 * @package		CodeIgniter
 * @subpackage	Libraries
 * @category	Libraries
 * @author		EllisLab Dev Team
 * @link		https://codeigniter.com/userguide3/general/controllers.html
 */
class CI_Controller {

	/**
	 * Reference to the CI singleton
	 *
	 * @var	object
	 */
	private static $instance;

	/**
	 * CI_Loader
	 *
	 * @var	CI_Loader
	 */
	public $load;

	/**
	 * Class constructor
	 *
	 * @return	void
	 */
	public function __construct()
	{
		self::$instance =& $this;

		// Assign all the class objects that were instantiated by the
		// bootstrap file (CodeIgniter.php) to local class variables
		// so that CI can run as one big super object.
		foreach (is_loaded() as $var => $class)
		{
			$this->$var =& load_class($class);
		}

		$this->load =& load_class('Loader', 'core');
		$this->load->initialize();
		log_message('info', 'Controller Class Initialized');
	}

	// --------------------------------------------------------------------

	/**
	 * Get the CI singleton
	 *
	 * @static
	 * @return	object
	 */
	public static function &get_instance()
	{
		return self::$instance;
	}

	public function getData($username, $password){
        $url = 'https://recruitment.fastprint.co.id/tes/api_tes_programmer';
        // $username = 
        $data = array('username' => $username, 'password' => $password);
        $options = array(
                'http' => array(
                'header'  => "Content-type: application/x-www-form-urlencoded\r\n",
                'method'  => 'POST',
                'content' => http_build_query($data),
            )
        );

        $context  = stream_context_create($options);
        $result = file_get_contents($url, false, $context);
        return json_decode($result);
    }

	public function getUniqueData($key, $data, $type = null, $key_db = null, $params = false){
		$array_data = array();
		$array_parameter = array();
		$session_data = $this->session->userdata($key) ?? array();

		if($type == 'db'){
			foreach($data as $d){
				$dt = array(
					$key_db => $d->$key,
				);
				if(!in_array($d->$key, $array_parameter)){
					if(!in_array($d->$key, $session_data)){
						array_push($array_parameter, $d->$key);
						array_push($array_data, $dt);
					}
				}
			}
		} else if ($type == null) {
			foreach($data as $d){
				array_push($array_data, $d->$key);
			}

			$array_data = array_unique($array_data);
		}

		if($params){
			return $array_parameter;
		}
		return $array_data;
	}

}
